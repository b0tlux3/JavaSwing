public class Detalle {
    
    String codTransaccion;
    String fecTransaccion;
    String cantTransaccion;
    String fecEntrega;
    String preMovimiento;
    String totMovimienot;
    String descProducto;
    String tipoTransaccion;

    public String getCodTransaccion() {
        return codTransaccion;
    }

    public void setCodTransaccion(String codTransaccion) {
        this.codTransaccion = codTransaccion;
    }

    public String getFecTransaccion() {
        return fecTransaccion;
    }

    public void setFecTransaccion(String fecTransaccion) {
        this.fecTransaccion = fecTransaccion;
    }

    public String getCantTransaccion() {
        return cantTransaccion;
    }

    public void setCantTransaccion(String cantTransaccion) {
        this.cantTransaccion = cantTransaccion;
    }

    public String getFecEntrega() {
        return fecEntrega;
    }

    public void setFecEntrega(String fecEntrega) {
        this.fecEntrega = fecEntrega;
    }

    public String getPreMovimiento() {
        return preMovimiento;
    }

    public void setPreMovimiento(String preMovimiento) {
        this.preMovimiento = preMovimiento;
    }

    public String getTotMovimienot() {
        return totMovimienot;
    }

    public void setTotMovimienot(String totMovimienot) {
        this.totMovimienot = totMovimienot;
    }

    public String getDescProducto() {
        return descProducto;
    }

    public void setDescProducto(String descProducto) {
        this.descProducto = descProducto;
    }

    public String getTipoTransaccion() {
        return tipoTransaccion;
    }

    public void setTipoTransaccion(String tipoTransaccion) {
        this.tipoTransaccion = tipoTransaccion;
    }
    
    
}
