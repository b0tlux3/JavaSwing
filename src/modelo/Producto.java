package modelo;

/**
 *
 * @author Usuario-Clone0
 */
public class Producto {
    String idProducto;
    String descProducto;
    String extraProducto;
    String nroProducto;
    String uniProducto;
    String preProducto;
    String stockProducto;

    public String getIdProducto() {
        return idProducto;
    }

    public void setIdProducto(String idProducto) {
        this.idProducto = idProducto;
    }

    public String getDescProducto() {
        return descProducto;
    }

    public void setDescProducto(String descProducto) {
        this.descProducto = descProducto;
    }

    public String getExtraProducto() {
        return extraProducto;
    }

    public void setExtraProducto(String extraProducto) {
        this.extraProducto = extraProducto;
    }

    public String getNroProducto() {
        return nroProducto;
    }

    public void setNroProducto(String nroProducto) {
        this.nroProducto = nroProducto;
    }

    public String getUniProducto() {
        return uniProducto;
    }

    public void setUniProducto(String uniProducto) {
        this.uniProducto = uniProducto;
    }

    public String getPreProducto() {
        return preProducto;
    }

    public void setPreProducto(String preProducto) {
        this.preProducto = preProducto;
    }

    public String getStockProducto() {
        return stockProducto;
    }

    public void setStockProducto(String stockProducto) {
        this.stockProducto = stockProducto;
    }
    
    
    
}
